public class ArrayStoryActivity{
    
    public static void main(String args[]){
        String fruits[] = {"Black", "You", "Are", "An", "Ship", "Excellent", "Can", "Save", "Programmer", "Two", "Wolf", "Sheep", "Sea"};
    
        for(int x = 0; x<fruits.length; x++){
            System.out.println(fruits[x]);
        }
    }

}