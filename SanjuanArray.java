public class SanjuanArray{
    public static void main(String args[]){
        
        //declaring flower array
        String flowers[] = {"hyacinth", "lily", "sampaguita", "daisy", "rose", "tulip"};
        
        //display all flowers
        for(int x=0; x<flowers.length; x++){
            System.out.println(flowers[x]);
        }
    }
}